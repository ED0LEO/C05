int	ft_sqrt(int nb)
{
	int	a;
	int	i;

	i = 2;
	a = 1;
	if (nb == 0)
		return (0);
	if (nb == 1)
		return (1);
	while (a < nb && i <= 46340)
	{
		a = i * i;
		i++;
	}
	if (a != nb)
		return (0);
	return (i - 1);
}
